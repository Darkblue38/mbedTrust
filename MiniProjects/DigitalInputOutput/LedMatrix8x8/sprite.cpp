enum {
  Low = 0,
  High = 1,
  MaxNumOfCol = 8,
  MaxNumOfRow = 8
};

uint8_t col = 0;
uint8_t leds[8][8];

const int numPatterns = 6;
uint8_t patterns[numPatterns][8][8] = {
  H,E,L,L,O,SPACE
};

// Hardware Digital Ouptut initialization
DigitalOut rows[8] = {PA_1, PA_0, PB_4, PA_3, PB_5, PA_8, PA_11, PB_3 };
DigitalOut cols[8] = {PB_6, PA_4, PB_7, PB_1, PA_9, PB_0, PA_10, PA_12 };

void clearLeds() {
  // Clear display array
  for (int i = 0; i < 8; i++) {
    for (int j = 0; j < 8; j++) {
      leds[i][j] = Low;
    }
  }
}

void setPattern(int pattern) {
  for (int i = 0; i < 8; i++) {
    for (int j = 0; j < 8; j++) {
      leds[i][j] = patterns[pattern][i][j];
    }
  }
}

void slidePattern(int pattern, int delay) {
  for (int l = 0; l < 8; l++) {
    for (int i = 0; i < 7; i++) {
      for (int j = 0; j < 8; j++) {
        leds[j][i] = leds[j][i+1];
      }
    }
    for (int j = 0; j < 8; j++) {
      leds[j][7] = patterns[pattern][j][0 + l];
    }
    wait_ms(delay);
  }
}

//Interrupt routine
void display() {
  cols[col] = Low;  // Turn whole previous column off
  col++;
  if (col == MaxNumOfCol) {
    col = 0;
  }
  for (int row = 0; row < MaxNumOfRow; row++) {
    if (leds[col][7 - row] == 1) {
      rows[row] = Low;  // Turn on this led
    }
    else {
      rows[row] = High; // Turn off this led
    }
  }
  cols[col] = High; // Turn whole column on at once (for equal lighting times)
}

// Ticker function is used to generate a periodic function call
Ticker flipper;

int main()
{
  int pattern = 0;

  flipper.attach_us(&display, 1000);
  setPattern(pattern);

  while(1)
  {
    pattern = ++pattern % numPatterns;
    slidePattern(pattern, 60);
  }
}
